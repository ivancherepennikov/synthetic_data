def get_temperament_divorce_adjust(type1, type2, scale=1.0):
    if type1 is None or type2 is None:
        return 0.0
    t1 = type1.lower() if isinstance(type1, str) else None
    t2 = type2.lower() if isinstance(type2, str) else None
    if t1 not in temperament_divorce_matrix or t2 not in temperament_divorce_matrix[t1]:
        return 0.0
    return temperament_divorce_matrix[t1][t2] * scale
